"use strict";
define("components/dialog.vue.js",[],function(){
Vue.component("mp-dialog",{
template:'\n  <transition name="dialog">\n    <mp-popup :title="title" :className="className" :mask="mask" :width="width" :height="height" @close="close" ref="popup">\n      <div slot="body">\n        <div :class="[\'page_msg\', \'large\', \'simple\', \'default\', msgClass]">\n          <div class="inner group">\n            <span class="msg_icon_wrapper"><i :class="[\'icon_msg\', icon]"></i></span>\n            <div class="msg_content">\n              <h4>{{msgTitle}}</h4>\n              <p><slot name="content"></slot></p>\n            </div>\n          </div>\n        </div>\n      </div>\n      <div slot="footer"><slot name="footer"></slot></div>\n    </mp-popup>\n  </transition>\n  ',
props:{
title:{
type:String,
"default":"温馨提示"
},
msgTitle:{
type:String,
"default":""
},
content:{
type:String,
"default":"对不起，系统繁忙请稍后尝试。"
},
className:{
type:String,
"default":""
},
width:{
type:Number,
"default":720
},
height:{
type:Number,
"default":0
},
type:{
type:String,
"default":"warn"
},
mask:{
type:Boolean,
"default":!0
}
},
data:function(){
return{
iconClass:{
suc:"success",
err:"error",
warn:"warn",
info:"info"
}
};
},
computed:{
icon:function(){
return this.iconClass[this.type]||"";
},
msgClass:function(){
return!this.msgTitle&&this.content?"single_line":"";
}
},
methods:{
close:function(){
this.$emit("close");
},
show:function(){
this.$refs.popup.show();
},
hide:function(){
this.$refs.popup.hide();
}
}
});
});